#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	while (n--)
	{
		*(char *)dst = *(char *)src;
		dst++;
		if (*(char *)src == (char)c)
			return ((void *)dst);
		src++;
	}
	return (NULL);
}
