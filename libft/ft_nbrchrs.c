#include "libft.h"

int	ft_nbrchrs(int n)
{
	int i;

	i = 0;
	if (n > 0)
		i = 1;
	else
		i = 2;
	while (n && n >= 10)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
